import os

os.environ['CUDA_VISIBLE_DEVICES']='1'
import tensorflow as tf
config = tf.ConfigProto()
config.gpu_options.per_process_gpu_memory_fraction = 0.2
sess = tf.InteractiveSession(config = config)
